public class Ternary {
    public static void main(String args[]){
        int a = 19;         // if condition is true it print first one 
        int b = 28;
        int c = (a<b)?a:b;
        System.out.println(c);
        int d = 29;
        int e = 82;              // if condition is false it print second one
        int f = (d>e)?d:e;
        System.out.println(f);
        
    }
}
